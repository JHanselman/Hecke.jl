function transvectant(f::MPolyRingElem{T}, g::MPolyRingElem{T}, k::Int) where T
  Kxy = parent(f)
  K = base_ring(Kxy)
  x, y = gens(Kxy)
  n = max(total_degree(f),k)
  m = max(total_degree(g),k)
  c = K(factorial(m-k) * factorial(n-k) // (factorial(m) * factorial(n)))

  Omega, (dfx, dfy, dgx, dgy) = polynomial_ring(K, ["dfx", "dfy", "dgx", "dgy"])
  diff_op = c * (dfx * dgy - dfy * dgx)^k

  result = Kxy(0)
  for mon in monomials(diff_op)
    dfxy_part = derivative(derivative(f, x, degree(mon, dfx)), y, degree(mon, dfy))
    dgxy_part = derivative(derivative(g, x, degree(mon, dgx)), y, degree(mon, dgy))
    
    result += coeff(diff_op,mon) * dfxy_part * dgxy_part
  end
  return result
end

function derivative(f::MPolyRingElem{T}, x::MPolyRingElem{T}, n::Int) where T
  @req n>=0 "n needs to be non-zero."
  i = 0
  while n > i 
    f = derivative(f, x)
    i +=1
  end
return f
end

function weighted_equality(w1, w2, ws)
  K = parent(w1[1])
  i = findall(x -> x != zero(K), w1)[1]
  if w2[i] == zero(K)
    return false
  else
    lambda_i = w1[i]/w2[i]
    Kx, x = polynomial_ring(K, "x") 
    f = x^ws[i] - lambda_i
    L = splitting_field(f)
    for a in roots(L, f)
      if all(x -> w1[x] == a^(ws[x])*w2[x], (1:length(ws)))
        return true
      end
    end
    return false
  end
end

function weighted_reduction(w1, ws)

    dens = map(denominator, w1)
    lambda = lcm(dens)
    Inorm = map(ZZ,[lambda^(ws[k]) * w1[k] for k in (1:length(w1))])
    primes = [ p for (p,e) in factor(gcd(Inorm)) ]

    Imin = Inorm
    for p in primes 
	    while all([valuation(Imin[k], p) >= ws[k] for k in (1:length(Imin)) ])
	      Imin = [ divexact(Imin[k], p^ws[k]) for k in (1:length(Imin)) ]
	    end
    end

    return Imin
end

