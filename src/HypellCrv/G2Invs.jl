function igusa_from_igusa_clebsch(igusa_clebsch_invs::Vector{T}) where T
  a, b, c, d = igusa_clebsch_invs
  J2  = a/8
  J4  = (4*J2^2 - b)/96
  J6  = (8*J2^3 - 160*J2*J4 - c)/576
  J8  = (J2*J6 - J4^2)/4
  J10 = d/4096
  return [J2, J4, J6, J8, J10]
end

function igusa_clebsch_from_igusa(igusa_invs::Vector{T}) where T
  J2, J4, J6, J8, J10 = igusa_invs
  a = 8*J2
  b = -96*J4 + 4*J2^2
  c = -576*J6 + 8*J2^3 - 160*J2*J4
  d = 4096 * J10
  return [a, b, c, d]
end

function  igusa_clebsch_from_clebsch(clebsch_invs::Vector{T}) where T
  A, B, C, D = clebsch_invs
  a = -120*A
  b = -720*A^2 + 6750*B
  c = 8640*A^3 - 108000*A*B + 202500*C
  d = -62208*A^5 + 972000*A^3*B + 1620000*A^2*C - 3037500*A*B^2 - 6075000*B*C -
   4556250 * D
   return [a, b, c, d]
end
 
function clebsch_from_igusa_clebsch(igusa_clebsch_invs::Vector{T}) where T
  a, b, c, d = igusa_clebsch_invs
  A = -a/120 ;
  B = (b+720*A^2)//6750
  C = (c-8640*A^3 + 108000*A*B)//202500 ;
  D = (d+62208*A^5 - 972000*A^3*B - 1620000*A^2*C + 3037500*A*B^2 +
   6075000*B*C)//(-4556250)
  return [A, B, C, D]
end

function g2_from_igusa(igusa_invs::Vector{T}) where T
    J2, J4, J6, J8, J10 = igusa_invs

    K = parent(J2)
    
    if characteristic(K) == 2
      if !is_perfect(K)
        error("The Cardona-Quer-Nart-Pujolas invariants are only defined over a field of characteristic 2 if it is perfect.")
      end
      if J2 != zero(K)
        g1 = sqrt(J10/J2^5)
        g2 = sqrt((J8/J2^4) - (J4/J2^2)^4 - (J4/J2^2)^3)
        g3 = Sqrt(J4/J2^2)
      elseif J6 != zero(K)
      g1 = zero(K)
      g2 = sqrt(J10^3/J6^5) 
      g3 = sqrt(sqrt(sqrt(J8*J10^4/J6^8)))
      else
        g1 = zero(K) 
        g2 = zero(K)
        g3 = sqrt(sqrt(sqrt(J8^5/J10^4)))
      end
    return [g1, g2, g3]
    end
	
    if J2 != zero(K)
	g1 = J2^5/J10
	g2 = J2^3*J4/J10
	g3 = J2^2*J6/J10
    elseif J4 != zero(K)
	g1 = zero(K)
	g2 = J4^5/J10^2
	g3 = J4*J6/J10
    else
      g1 = zero(K)
      g2 = zero(K)
      g3 = J6^5/J10^3
    end
    return [g1, g2, g3]
end

function igusa_from_g2(g2_invs::Vector{T}) where T
  g1, g2, g3 = g2_invs
  K = parent(g1)
  if characteristic(K) == 2
    if !is_perfect(K)
      error("The Cardona-Quer-Nart-Pujolas invariants are only defined over 
      a field of characteristic 2 if it is perfect.")
    end
    if g1 != zero(K)
      J2 = one(K)
      J4 = g3^2
      J6 = g3^4
      J8 = g2^2+g3^8+g3^6
      J10 = g1^2
    elseif g2 != zero(K)
      J2 = J4 = zero(K)
      J6 = g2^2
      J8 = g3^8
      J10 = g2^4
    elseif g3 != zero(K)
      J2 = J4 = J6 = zero(K)
      J8 = g3^4
      J10 = g3^3
    else
      J2 = J4 = J6 = J8 = zero(K)
      J10 = K(1)
    end
  return [J2, J4, J6, J8, J10]
  end

  if g1 != zero(K)
    J2 = g1
    J4 = g1*g2
    J6 = g1^2*g3
    J8 = (g1^3*g3-g1^2*g2^2)//4
    J10 = g1^4
  elseif g2 != zero(K)
    J2 = zero(K)
    J4 = g2
    J6 = g2*g3
    J8 = -g2^2//4
    J10 = g2^2
  elseif g3!=zero(K)
    J2 = J4 = zero(K)
    J6 = g3^2
    J8 = zero(K)
    J10 = g3^3
  else
    J2 = J4 = J6 = J8 = zero(K)
    J10 = K(1)
  end
  return [J2, J4, J6, J8, J10]
end


